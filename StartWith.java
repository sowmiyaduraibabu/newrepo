package day3Stringmethod;

public class StartWith {
	public static void main(String[] args) {
		String name="Sowmiya";
		boolean starts=name.startsWith("Sow");
		boolean starts1=name.startsWith("ow");
		System.out.println(name + "Starts with Sow: " +starts + " \n " +name + "Start with ow: " +starts1);
	}

}
